@Service("tourService")
public class TourServiceImpl extends EgovAbstractServiceImpl implements TourService {

	private static final Logger LOGGER = LoggerFactory.getLogger(TourServiceImpl.class);

	/** SampleDAO */
	// TODO ibatis 사용
	@Resource(name = "tourDAO")
	private TourDAO tourDAO;
	// TODO mybatis 사용
	//  @Resource(name="sampleMapper")
	//	private SampleMapper sampleDAO;

	/** ID Generation */
	@Resource(name = "egovIdGnrService")
	private EgovIdGnrService egovIdGnrService;

	/**
	 * 글을 조회한다.
	 * @param vo - 조회할 정보가 담긴 SampleVO
	 * @return 조회한 글
	 * @exception Exception
	 */
	@Override
	public TourVO selectTour(TourVO vo) throws Exception {
		TourVO tourVO = tourDAO.selectTour(vo);
		if (tourVO == null)
			throw processException("info.nodata.msg");
		return tourVO;
	}
	
	@Override
	public CityVO selectCityNames(CityVO vo) throws Exception {		
		CityVO cityVO = tourDAO.selectCityNames(vo);		
		return cityVO;
	}
	
	/**
	 * 글 목록을 조회한다.
	 * @param searchVO - 조회할 정보가 담긴 VO
	 * @return 글 목록
	 * @exception Exception
	 */
	@Override
	public List<?> selectTourList(CityVO cityVO) throws Exception {
		return tourDAO.selectTourList(cityVO);
	}

	/**
	 * 글 총 갯수를 조회한다.
	 * @param searchVO - 조회할 정보가 담긴 VO
	 * @return 글 총 갯수
	 * @exception
	 */
	@Override
	public int selectTourListTotCnt(SampleDefaultVO searchVO) {
		return tourDAO.selectTourListTotCnt(searchVO);
	}
}